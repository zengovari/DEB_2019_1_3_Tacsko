package Model;

public class City {

    private int id;
    private String name;
    private String country;
    private Coord coord;


    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getCountry() {
        return country;
    }

    public Coord getCoord() {
        return coord;
    }


}
